        mock_s3 = MagicMock()
        mock_s3.get_object.side_effect = Exception("S3 unavailable")
        mock_boto_client.return_value = mock_s3

        # Mock WeasyPrint: return fake PDF bytes starting with %PDF
        fake_pdf_bytes = b'%PDF-1.4 fake pdf content for testing purposes'
        mock_weasyprint_html = MagicMock()
        mock_weasyprint_html.return_value.write_pdf.return_value = fake_pdf_bytes

        with patch('app.weasyprint') as mock_wp:
            mock_wp.HTML = mock_weasyprint_html

