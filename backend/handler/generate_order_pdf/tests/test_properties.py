class TestS3ImageToDataUriRoundTrip:
    """Property 2: S3 image to data URI round-trip

    For any byte sequence and for any valid MIME content type string,
    encoding the bytes to a data URI and then decoding the base64 portion
    of that URI SHALL produce the original byte sequence, and the URI SHALL
    match the format `data:{content_type};base64,{encoded_data}`.

    **Validates: Requirements 3.2**
    """

    @given(
        image_bytes=st.binary(min_size=1, max_size=10000),
        content_type=mime_types,
    )
    @settings(max_examples=100)
    @patch('app.boto3.client')
    def test_data_uri_round_trip(self, mock_boto_client, image_bytes, content_type):
        """For any byte sequence and valid MIME type, encoding to data URI and
        decoding the base64 portion produces the original bytes, and the URI
        matches the expected format.

        **Validates: Requirements 3.2**
        """
        # Arrange: mock S3 to return the generated bytes with the generated content type
        mock_s3 = MagicMock()
        mock_boto_client.return_value = mock_s3
        mock_body = MagicMock()
        mock_body.read.return_value = image_bytes
        mock_s3.get_object.return_value = {
            'ContentType': content_type,
            'Body': mock_body,
        }

        # Act
        result = fetch_logo_as_data_uri('test-bucket', 'test-key')

        # Assert: result is not None
        assert result is not None

        # Assert: URI matches the format data:{content_type};base64,{encoded_data}
        expected_prefix = f"data:{content_type};base64,"
        assert result.startswith(expected_prefix), (
            f"Data URI should start with '{expected_prefix}', got: {result[:80]}"
        )

        # Assert: the full URI matches the regex pattern
        pattern = re.compile(r'^data:[^;]+;base64,[A-Za-z0-9+/]+=*$')
        assert pattern.match(result), (
            f"Data URI does not match expected format: {result[:80]}"
        )

        # Assert: decoding the base64 portion produces the original byte sequence
        encoded_data = result[len(expected_prefix):]
        decoded_bytes = base64.b64decode(encoded_data)
        assert decoded_bytes == image_bytes, (
            f"Round-trip failed: decoded {len(decoded_bytes)} bytes, "
            f"expected {len(image_bytes)} bytes"
        )


# --- Strategies for Property 1 ---

# Roles that grant products_create permission (with appropriate region access)
# System_CRUD and System_User_Management get immediate full access (no region needed)
# Products_CRUD and Webshop_Management need a region role to pass validate_permissions_with_regions
ADMIN_ROLES_FULL_ACCESS = ['System_CRUD', 'System_User_Management']
ADMIN_ROLES_NEED_REGION = ['Products_CRUD', 'Webshop_Management']
ALL_ADMIN_ROLES = ADMIN_ROLES_FULL_ACCESS + ADMIN_ROLES_NEED_REGION

# Region roles that satisfy the region requirement
REGION_ROLES = ['Regio_All', 'Regio_Noord', 'Regio_Zuid', 'Regio_Oost', 'Regio_West']

# All possible roles in the system that do NOT grant products_create
NON_ADMIN_ROLES = [
    'Members_CRUD', 'Members_Read', 'Members_Export',
    'Events_CRUD', 'Events_Read', 'Events_Export',
    'Products_Read', 'Products_Export',
    'Communication_CRUD', 'Communication_Read', 'Communication_Export',
    'Members_Status_Approve',
    'System_Logs_Read',
    'hdcnLeden', 'verzoek_lid',
]

# Email strategy: generates valid email-like strings
email_strategy = st.from_regex(
    r'[a-zA-Z][a-zA-Z0-9_.]{0,20}@[a-zA-Z]{1,10}\.[a-zA-Z]{2,4}', fullmatch=True
)

# Role set strategy: random subset of all roles (including region roles)
role_strategy = st.lists(
    st.sampled_from(ALL_ADMIN_ROLES + NON_ADMIN_ROLES + REGION_ROLES),
    min_size=0,
    max_size=5,
    unique=True
)


def _has_products_create_permission(roles: list) -> bool:
    """
    Determine if a set of roles grants the products_create permission,
    taking into account the region requirement from validate_permissions_with_regions.

    Based on the role-to-permission mapping in auth_utils.py:
    - System_CRUD has wildcard '*' (grants everything, no region needed)
    - System_User_Management gets immediate full access in validate_permissions_with_regions
    - Products_CRUD has 'products_create' (needs region role)
    - Webshop_Management has 'products_create' (needs region role)

    The validate_permissions_with_regions function requires:
    - System_CRUD/System_User_Management: immediate full access
    - Permission roles with region roles: authorized
    - hdcnLeden/verzoek_lid without region: basic member access (authorized but no products_create)
    - Permission roles without region: denied (requires region assignment)
    """
    # System_CRUD or System_User_Management gets immediate full access
    if any(role in ADMIN_ROLES_FULL_ACCESS for role in roles):
        return True

    # Check if user has a permission role that grants products_create
    has_permission_role = any(role in ADMIN_ROLES_NEED_REGION for role in roles)
    if not has_permission_role:
        return False

    # Permission roles need a region role to pass validate_permissions_with_regions
    has_region = any(role in REGION_ROLES for role in roles)
    # hdcnLeden/verzoek_lid count as basic member (pass without region)
    has_basic_member = any(role in ['hdcnLeden', 'verzoek_lid'] for role in roles)

    return has_region or has_basic_member


def _is_authorized(user_email: str, order_email: str, user_roles: list) -> bool:
    """
    Reference implementation of the authorization logic.

    Access is granted if and only if:
    1. The user's email matches the order's user_email (case-insensitive), OR
    2. The user has the products_create permission (via Products_CRUD, Webshop_Management, or System_CRUD)
       with appropriate region access
    """
    email_matches = user_email.lower() == order_email.lower()
    has_permission = _has_products_create_permission(user_roles)
    return email_matches or has_permission


# Feature: order-confirmation-pdf, Property 1: Authorization grants access if and only if user is owner or admin
class TestProperty1AuthorizationGrantsAccess:
    """Property 1: Authorization grants access if and only if user is owner or admin.

    Validates: Requirements 2.2, 2.3, 2.4

    For any order record with an associated user_email, and for any authenticated
    user with an email and set of roles: the PDF generation request SHALL be
    authorized if and only if the user's email matches the order's user_email
    (case-insensitive) OR the user has the products_create permission (via
    Products_CRUD, Webshop_Management, or System_CRUD roles).
    """

    @given(
        user_email=email_strategy,
        order_email=email_strategy,
        user_roles=role_strategy,
    )
    @settings(max_examples=200)
    def test_owner_access_granted_when_emails_match(self, user_email, order_email, user_roles):
        """When user email matches order email (case-insensitive), access is always granted.

        **Validates: Requirements 2.2**
        """
        # Force emails to match (case-insensitive)
        order_email = user_email.upper()

        # The user is the owner, so authorization should always succeed
        assert _is_authorized(user_email, order_email, user_roles) is True

    @given(
        user_email=email_strategy,
        order_email=email_strategy,
        admin_role=st.sampled_from(ALL_ADMIN_ROLES),
        extra_roles=st.lists(st.sampled_from(NON_ADMIN_ROLES + REGION_ROLES), min_size=0, max_size=3, unique=True),
    )
    @settings(max_examples=200)
    def test_admin_access_granted_with_products_create_permission(
        self, user_email, order_email, admin_role, extra_roles
    ):
        """When user has products_create permission (via Products_CRUD, Webshop_Management,
        or System_CRUD), access is always granted regardless of email match.

        **Validates: Requirements 2.3**
        """
        # Ensure emails don't match to isolate the admin permission check
        assume(user_email.lower() != order_email.lower())

        user_roles = [admin_role] + extra_roles

        # If admin_role needs a region, ensure one is present
        if admin_role in ADMIN_ROLES_NEED_REGION:
            if not any(r in REGION_ROLES for r in user_roles):
                user_roles.append('Regio_All')

        # Admin role grants access regardless of email
        assert _is_authorized(user_email, order_email, user_roles) is True

    @given(
        user_email=email_strategy,
        order_email=email_strategy,
        user_roles=st.lists(
            st.sampled_from(NON_ADMIN_ROLES + REGION_ROLES),
            min_size=0, max_size=5, unique=True
        ),
    )
    @settings(max_examples=200)
    def test_access_denied_when_not_owner_and_not_admin(self, user_email, order_email, user_roles):
        """When user email does NOT match order email AND user does NOT have
        products_create permission, access is denied.

        **Validates: Requirements 2.4**
        """
        # Ensure emails don't match
        assume(user_email.lower() != order_email.lower())

        assert _is_authorized(user_email, order_email, user_roles) is False

    @given(
        user_email=email_strategy,
        order_email=email_strategy,
        user_roles=role_strategy,
    )
    @settings(max_examples=200)
    def test_authorization_biconditional_matches_real_auth_layer(
        self, user_email, order_email, user_roles
    ):
        """The authorization decision from the reference implementation matches
        the actual auth layer's validate_permissions_with_regions for the
        products_create permission check.

        This verifies the biconditional: access is granted IF AND ONLY IF
        email matches OR user has products_create permission.

        **Validates: Requirements 2.2, 2.3, 2.4**
        """
        # Check email match (case-insensitive)
        email_matches = user_email.lower() == order_email.lower()

        # Check permission via the real auth layer
        is_perm_authorized, error_response, _ = _real_validate_permissions(
            user_roles, ['products_create'], user_email=user_email
        )

        # The combined authorization decision
        actual_authorized = email_matches or is_perm_authorized

        # Must match our reference implementation
        expected_authorized = _is_authorized(user_email, order_email, user_roles)

        assert actual_authorized == expected_authorized, (
            f"Authorization mismatch: user_email={user_email}, order_email={order_email}, "
            f"roles={user_roles}, email_matches={email_matches}, "
            f"perm_authorized={is_perm_authorized}, expected={expected_authorized}, "
            f"actual={actual_authorized}"
        )


